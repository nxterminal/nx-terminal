import { Coins, Image, Layers, Lock, Gamepad2, Code2 } from 'lucide-react';
import { useBuild } from '../../BuildContext';
import { CONTRACT_TYPES } from '../../constants/monad';
import Card from '../shared/Card';
import Badge from '../shared/Badge';

const ICONS = {
  erc20: Coins,
  erc721: Image,
  erc1155: Layers,
  staking: Lock,
  game: Gamepad2,
  custom: Code2,
};

export default function TemplateGallery({ onSelect }) {
  const { dispatch } = useBuild();

  function handleSelect(type) {
    if (type === 'custom') {
      dispatch({ type: 'SET_CONTRACT_TYPE', payload: 'custom' });
      dispatch({ type: 'SET_CODE', payload: `// SPDX-License-Identifier: MIT
// Generated by MONAD_BUILD.exe — NX Terminal
// Compiler: solc ^0.8.28, evmVersion: "prague"

pragma solidity ^0.8.28;

contract MyContract {
    constructor() {}
}
` });
      onSelect('editor');
      return;
    }
    dispatch({ type: 'SET_CONTRACT_TYPE', payload: type });
    onSelect('wizard');
  }

  return (
    <div>
      <h2 className="mb-h2 mb-mb-sm">Choose a Template</h2>
      <p className="mb-text-sm mb-mb-lg">
        Select a contract template to get started, or write from scratch.
      </p>

      <div className="mb-grid-3">
        {CONTRACT_TYPES.map(type => {
          const Icon = ICONS[type.id];
          return (
            <Card key={type.id} onClick={() => handleSelect(type.id)} gradient>
              <div style={{
                width: 44,
                height: 44,
                borderRadius: 12,
                background: 'rgba(131,110,249,0.1)',
                display: 'flex',
                alignItems: 'center',
                justifyContent: 'center',
                marginBottom: 12,
              }}>
                <Icon size={22} style={{ color: 'var(--mb-accent-primary)' }} />
              </div>
              <div style={{ fontWeight: 600, fontSize: 15, marginBottom: 4 }}>{type.name}</div>
              <div className="mb-text-sm mb-mb-sm">{type.description}</div>
              <div className="mb-flex mb-gap-sm">
                {type.tags.map(tag => (
                  <Badge key={tag} color="purple">{tag}</Badge>
                ))}
              </div>
            </Card>
          );
        })}
      </div>
    </div>
  );
}
